#operators
x=1
y=2
print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x//y)
print(x%y)
print(x**y)
#Relational Operators
print("Relational Operators")
print(x!=y)
print(x==y)
print(x>y)
print(x<y)
print(x>=y)
print(x<=y)
#logical operators
print("logical operators")
print(x<y and x<10)
print(x<y or x<10)
print(not x)
#Bitwise Operator
print("Bitwise Operator")
print(x&y)
print(x|y)
print(x^y)
print(~x)
print(~y)
print(x<<y)
print(x>>y)
#Assignment Operator
print("Assignment Operator")
x+=2
print(x)
x-=2
print(x)
x/=2
print(x)
x*=3;
print(x)
#Membership Operator
print("Membership Operator")
str="Abcde"
print('a' in str)
print('a' not in str)
print('A' in str)
#identity operator
print("identity operator")
print(1 is int)
print(type(y) is not int)
#if else
print("if else")
a=10
b=9
print(a if a>b else b)
